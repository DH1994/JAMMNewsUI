    private void refreshList()
    {
        locList.removeAll();
        delayList.removeAll();
        
        for (int dly : listDly) 
        {
            delayList.add(dly + "");          
        }
        
        for (String input : listInputs) 
        {
            locList.add(input);
        }       
    }
    
    private void clearFields()
    {
        listDly.clear();
        listInputs.clear();
        refreshList();
        
        outputField.setText("");
        delayField.setText("");  
    }
    
