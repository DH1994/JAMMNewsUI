    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        
        try
        {
            Integer.parseInt(delayField.getText());
        }
        catch(Exception e) {
            System.out.println(e);
            delayField.setText("0");
        }
        
        if(locField.getText().length() > 0)
                
        {
            listDly.add(Integer.parseInt(delayField.getText()));
            listInputs.add(locField.getText());
            refreshList();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Fill all fields!");
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        
        if(outputField.getText().length() > 0 && locList.getItemCount() > 1)
        {
            Mix m = new Mix();
            for (int dly : listDly) 
            {
                m.setDelays(dly);
            }
            
            for (String input : listInputs) 
            {
                m.setInputs(input);
            }
            
            
            m.setOutput(outputField.getText());
            m.setDuration(durationCbox.getSelectedItem().toString());
            
            if(edit)
            {
                parent.editTaskHandler(m, index);
                edit = false;
                clearFields();
                this.dispose();
            }
            else
            {
                parent.addTaskHandler(m);
                clearFields();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Fill all fields!");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

